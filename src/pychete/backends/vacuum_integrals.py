LoopFunctionPower: TypeAlias = Expression | int
_LOGGER = get_logger("backends.vacuum_integrals")


def epsilon_symbol() -> Expression:
    """Return the dimensional regulator used by pychete's vacuum-integral series."""

    return S("vakint::ε")


def mu_r_squared_symbol() -> Expression:
    """Return the renormalization-scale-squared symbol used in vacuum-integral series."""

    return S("vakint::mursq")


def imaginary_unit_symbol() -> Expression:
    """Return the native Symbolica imaginary unit used by pychete results."""

    return Expression.I


def loop_function(masses: tuple[Expression, ...], powers: tuple[LoopFunctionPower, ...]) -> Expression:
    """Return pychete's Matchete-style finite loop-function placeholder.

    The convention mirrors Matchete's ``LF[{m1, ...}, {i1, ..., alpha}]``:
    ``masses`` are masses rather than mass-squared slots, ``powers[:-1]`` are
    massive propagator powers, and ``powers[-1]`` is the massless
    ``1/k^2`` power generated by numerator reduction.
    """

    power_exprs = tuple(_power_expression(power) for power in powers)
    _validate_loop_function_lengths(masses, power_exprs)
    return s.LoopFunction(list_expr(*masses), list_expr(*power_exprs))


def canonize_loop_function(expr: Expression) -> Expression:
    """Canonize one pychete ``LoopFunction`` atom.

    This mirrors Matchete's local ``LF`` atom normalization: repeated masses
    have their massive propagator powers summed, zero massive powers are
    removed, and remaining massive propagators are ordered by descending power
    and then by canonical mass name. The final massless ``1/k^2`` power is
    preserved. Purely scaleless massless remnants canonize to zero.
    """

    masses, powers = _loop_function_data(expr)
    alpha = powers[-1]
    mass_powers = _canonized_mass_powers(zip(masses, powers[:-1], strict=True))
    if not mass_powers:
        return Expression.num(0)
    return loop_function(
        tuple(mass for mass, _power in mass_powers),
        (*tuple(power for _mass, power in mass_powers), alpha),
    )


def canonize_loop_functions(expr: Expression, *, combine_terms: bool = False) -> Expression:
    """Canonize all pychete ``LoopFunction`` atoms in an expression.

    Replacement is delegated to Symbolica's native wildcard matcher. When
    ``combine_terms`` is set, the resulting expression is passed through
    ``Expression.together()`` so rational coefficients multiplying canonized
    loop functions can combine deterministically.
    """

    pattern = _loop_function_pattern()
    for match in expr.match(pattern):
        _loop_function_data(pattern.replace_wildcards(match))

    def canonize_match(match: dict[Expression, Expression]) -> Expression:
        return canonize_loop_function(pattern.replace_wildcards(match))

    return _finish_evaluated_expression(
        expr.replace(pattern, canonize_match).expand(),
        combine_terms=combine_terms,
    )


def loop_function_pole_part(
    expr: Expression,
    *,
    epsilon: Expression | None = None,
    max_pole_order: int = 1,
) -> Expression:
    """Return the epsilon pole part associated with one finite ``LoopFunction`` atom.

    Matchete's loop-function IBP identities are derived for full loop
    functions. When reducing finite ``LF`` placeholders, the epsilon-suppressed
    pieces generated by IBP can multiply pole terms and leave finite remnants.
    This helper extracts those pole terms from pychete's internal analytic
    one-loop evaluator using Symbolica coefficient extraction.
    """

    loop_expr = canonize_loop_function(expr)
    if bool(loop_expr == Expression.num(0)):
        return Expression.num(0)
    integral = loop_function_to_vakint_integral(loop_expr)
    evaluated = evaluate_one_loop_vakint_expression(
        integral,
        epsilon=epsilon,
        combine_terms=False,
    )
    return vakint.pole_part(
        (evaluated / imaginary_unit_symbol()).expand(),
        epsilon=epsilon,
        max_pole_order=max_pole_order,
    )


def reduce_loop_function_first_power(
    expr: Expression,
    *,
    target_mass_index: int = 0,
    epsilon: Expression | None = None,
    max_pole_order: int = 1,
    max_steps: int = 100,
) -> Expression:
    """Reduce one ``LoopFunction`` atom so one selected massive power is one.

    This is a focused pychete counterpart of Matchete's ``SimpTempLFRules``:
    the selected massive propagator is moved into the first slot, the native
    Symbolica replacement engine applies the IBP rule until that first power is
    one, and the finite epsilon coefficient is extracted after inserting the
    needed loop-function pole pieces. Broader mass-function optimization across
    sums is intentionally left to later matching slices.
    """

    loop_expr = canonize_loop_function(expr)
    if bool(loop_expr == Expression.num(0)):
        return Expression.num(0)
    masses, powers = _loop_function_data(loop_expr)
    if not 0 <= target_mass_index < len(masses):
        raise ValueError("target_mass_index is out of range for LoopFunction masses")
    if len(masses) < 2 or powers[target_mass_index] <= 1:
        return loop_expr
    current = _swap_loop_function_masses(loop_expr, target_mass_index, 0)
    pattern = _loop_function_pattern()
    regulator = epsilon_symbol() if epsilon is None else epsilon
    for _step in range(max_steps):
        if not _has_reducible_first_loop_function_power(current):
            break
        current = current.replace(
            pattern,
            lambda match: _reduce_first_power_match(
                pattern.replace_wildcards(match),
                epsilon=regulator,
            ),
        ).expand()
    else:
        raise RuntimeError("LoopFunction first-power reduction did not converge")
    return canonize_loop_functions(
        _finite_loop_function_expression(
            current,
            epsilon=regulator,
            max_pole_order=max_pole_order,
        ),
        combine_terms=True,
    )


